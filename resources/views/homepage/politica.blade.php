    <h2>1) Quais dados coletamos</h2>
    <ul>
      <li><strong>Dados de identificação e contato:</strong> nome, número de WhatsApp/telefone, e-mail (quando informado).</li>
      <li><strong>Dados de origem/marketing:</strong> parâmetros de URL (ex.: <code>utm_source</code>, <code>utm_medium</code>, <code>utm_campaign</code>), página de referência, data e horário do cadastro.</li>
