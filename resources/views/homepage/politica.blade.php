      <li>Portar seus dados a outro fornecedor, mediante requisição expressa;</li>
      <li>Eliminar dados tratados com consentimento, quando aplicável;</li>
      <li>Obter informação sobre o compartilhamento e as consequências da negativa;</li>
      <li>Revogar o consentimento e se opor ao tratamento quando cabível.</li>
    </ul>
    <p>Para exercer seus direitos, envie um e-mail para <a href="mailto:contato@3f7.org">contato@3f7.org</a> com o assunto <em>“Direitos do Titular — LGPD”</em>.</p>
