      <li><strong>Melhoria contínua:</strong> analisar uso do site e das campanhas para aprimorar experiência, segurança e performance.</li>
      <li><strong>Cumprimento legal:</strong> atender obrigações legais/regulatórias e exercer direitos em processos judiciais ou administrativos.</li>
      <li><strong>Segurança:</strong> prevenir fraudes, abusos e incidentes, e manter a integridade da nossa plataforma e dos usuários.</li>
    </ul>
