      <li><strong>Provedores de hospedagem, infraestrutura e e-mail/WhatsApp</strong> (ex.: serviços de nuvem, mensageria, envio de notificações) para operação da plataforma.</li>
      <li><strong>Ferramentas de analytics e anúncios</strong> (quando habilitadas por você) para mensurar e otimizar campanhas.</li>
      <li><strong>Autoridades públicas</strong> quando exigido por lei, ordem judicial ou para proteger direitos.</li>
