  <section>
    <h2>3) Bases legais (LGPD)</h2>
    <p>Tratamos dados pessoais com fundamento nas seguintes bases legais, conforme o caso:</p>
    <ul>
      <li><strong>Consentimento</strong> (art. 7º, I) — para envio de comunicações de marketing e participação em grupos de WhatsApp.</li>
      <li><strong>Execução de contrato</strong> (art. 7º, V) — para fornecer acesso a produtos/serviços adquiridos ou solicitados.</li>
      <li><strong>Legítimo interesse</strong> (art. 7º, IX) — para melhorias do site, prevenção a fraudes, métricas de uso e segurança (sempre com avaliação de impacto e respeito aos seus direitos).</li>
      <li><strong>Cumprimento de obrigação legal/regulatória</strong> (art. 7º, II) — quando aplicável.</li>
    </ul>
  </section>

